// Function to create a staggered container animation with custom parameters
export const createStaggerContainer = (stagger = 0.1, delay = 0.3) => ({
  hidden: { opacity: 0 },
  show: {
    opacity: 1,
    transition: {
      staggerChildren: stagger,
      delayChildren: delay
    }
  }
})
